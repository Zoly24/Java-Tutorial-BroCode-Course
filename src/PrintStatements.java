public class PrintStatements {
    public static void main (String[] args) {
        System.out.println("Hello World!\n");   //Prints out Hello World! with two new lines, one from println and one from \n
        System.out.println("Bye World!");       //Prints out Bye World! with one new line from println

        /* \n = New Line
         * \t = Tab 
         * \\ = backslash
         * \" Quotation Mark
         * \b = Insert Backscape
         * \' = Insert Single Quotation Mark
         * \r = carriage return.()
         *
         * println = Prints string then adds a new line
         * print = Prints string without adding a new line
         */
    }
}